                                  <h3>About Procedure</h3>
                              <p>When sampling peripheral pulmonary nodule via a bronchoscope, it is important to obtain adequate tissue for analysis. Forceps biopsy of nodule is many times insufficient, and the diagnostic yield is sub optimal. Cryobiopsy of the nodules is a new technique to improve the diagnostic yield. After visualizing the nodule with radial EBUS, a flexible cryoprobe (1.7mm or 1.1mm) is passed through the working channel of the scope, and cryobiopsy(ies) obtained from the nodule. To ensure real time visualization, fluoro guidance is often employed. The procedure is performed via rigid bronchoscope and using a fogarty occlusion balloon.</p>
