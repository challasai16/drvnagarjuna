                      <h3>About Procedure</h3>
                      <p>Linear EBUS is now the preferred diagnostic procedure for sampling mediastinal masses or lymph nodes. It has replaced surgical mediastinoscopy, as it has the same diagnostic accuracy and is much safer. In this procedure, a dedicated EBUS scope is introduced into the airway and ultrasound of the mediastinum is performed. After identifying the target node/mass, a needle is introduced into the target and sampling is done by real time visualization of the needle within the target node/mass.</p>
                          <br>
                      <h3>Indication for Linear EBUS</h3>
                      <ul>
                          <li>1. Diagnosis of any mediastinal mass/node which abutts the airway. </li>
                          <li>2. Staging EBUS (mediastinal staging) for early stage lung cancer.</li>
                      </ul>
                      <br>
                      <h3>Achievements/Accolades</h3>
                      <ul>
                          <li>1. The centre performs more than 200 linear EBUS procedures every year, a high volume centre for linear EBUS. </li>
                          <li>2. Introduced several new techniques for performing intra- nodal biopsy from mediastinal nodes.</li>
                      </ul>
                      <br>
                      <h3>Case Vignette</h3>
                      <p>A 72 year gentleman, treated case of carcinoma colon was incidentally diagnosed to have a sub- centimetric paratracheal node. Linear EBUS guided sampling of the node perfomed confirmed the diagnosis of distant recurrence.</p>
                      
