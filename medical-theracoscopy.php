                      <h3>Case Vignette</h3>
                      <p><strong>Case 1:</strong> A 45 year male presented with fever and dyspnea over 2 weeks. Chest x ray showed a large loculated pleural effusion. He underwent medical thoracoscopy which revealed thick adhesions all over. Adhesiolysis was performed using a mini-rigid thoracoscope. Two litres of pleural fluid was drained and a biopsy performed from the parietal pleura. Biopsy confirmed it to be a synpneumonic effusion. Chest radiograph post procedure showed complete lung expansion.</p>
                      
