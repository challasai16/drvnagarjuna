                                  <h3>About Procedure</h3>
                              <p>Medical thoracoscope is a scope/instrument which is passed into the pleural cavity through a small incision on the lateral chest wall. This is performed by a pulmonologist under local anesthesia and sedation. Once the scope enters the pleural cavity, the fluid is drained and the visceral and parietal pleura examined. Biopsies are performed from any abnormal areas on the parietal pleura. The diagnostic yield of medical thoracoscopic pleural biopsy is >98% and is much superior to closed pleural biopsy. The same instrument can also be used to perform adhesiolysis in loculated pleural effusions and to perform talc poudrage pleurodesis.</p>
