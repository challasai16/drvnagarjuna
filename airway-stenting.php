                                  <h3>About Procedure</h3>
                              <p>An airway stent is an endobronchial/endotracheal prosthesis that supports and maintains patency of the airways. The main classes of stents that are currently being used are silicone stents (benign diseases) and covered metallic stents (malignant stenoses). Procedure is generally done under general anaesthesia and rigid bronchoscopy. Covered metallic stents can be placed under conscious sedation using flexible bronchoscope. After sufficient tracheal diameter is achieved using various modalities, the size and type of stent is decided and placed. A hitch stitch maybe placed to prevent stent migration.</p>
