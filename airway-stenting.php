                      <h3>Case Vignette</h3>
                      <p><b>Case 1:</b> 55-year lady, with rapidly worsening stridor for 1 month, was referred to us for management. She had stridor at rest. Bronchoscopy showed growth in lower trachea and proximal parts of both main bronchi, with near complete occlusion of right main bronchus. Rigid bronchoscopic removal of tumor was performed using the coring technique and cryodebulking. This was followed by placement of a metallic 'Y' shaped self-expanding metallic stent (SEMS) of 16 x 12 x 12mm size. There was immediate relief in stridor. Biopsy confirmed adenoid cystic carcinoma.</p>
                      
