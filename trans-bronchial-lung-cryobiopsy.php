                                  <h3>About Procedure</h3>
                              <p>Trans bronchial lung crobiopsy (TBLC) is a biopsy technique for obtainng larger lung tissue specimens via bronchoscopy. This has replaced surgical lung biopsy as the modality of choice for interstitial lung disease diagnosis. The procedure is performed under anesthesia using rigid bronchoscope as the conduit. The flexible bronchoscope is passed to the target segment, and a cryoprobe (1.7mm,1.9mm or 2.4 mm) then advanced through the working channel. Under fluoro guidance, 3 to 6 cryobiopsies are obtained from different segments. An occlusion balloon is also used to prevent spill over of bleed into other segments. The diagnostic yield of a TBLC is comparable to surgical lung biopsy.</p>
