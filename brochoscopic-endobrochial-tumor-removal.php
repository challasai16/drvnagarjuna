                      <p><b>Case 2:</b> A 30-year male, on anti-tubercular therapy, presented with worsening breathlessness. Bronchoscopy showed tumors causing near complete occlusion of the opening of both main bronchi. The tumors were removed using a combination of electrocautery snare, forceps and a cryoprobe. Histopathology confirmed granulomas and exuberant granulation tissue (Tumorous endo-bronchial tuberculosis). ATT was continued and the index case gradually recovered.</p>
                      
