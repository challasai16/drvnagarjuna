                      <p><b>Case 1:</b> A 60 year man presented with a non-resolving left lower lobe pneumonia. CT and bronchoscopy showed a lesion in the lumen of the left main bronchus. Endobronchial removal of the lesion performed which confirmed it to be a endobronchial chondrohamartoma.</p>
                      
