                              <p>Tumours located in the central airway can cause central airway obstruction leading to morbidity and mortality. Tumours which are located purely within the airway lumen can be removed in-toto via bronchoscopy. This procedure called as bronchoscopic surgery is a scar less procedure. Procedure is performed via rigid bronchoscopy. Several tools such as cryoprobe, electrocautery accessories, rigid bronchoscopic forceps are used to remove the tumor depending on the type and the site of the tumor.</p>
